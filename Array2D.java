public class Array2D {

	public static void main(String args[])
	{
		 int a[][]= {
				 {2,9,8,5,7,6,4,3,1} ,
				 {1,3,5,7,8,9,6,5}
				
		 };
		 for(int i=0;i<a.length;i++)
		  {
			  
			  for(int j = 0; j<a[i].length;j++)
			  {
				  System.out.println(j );
				  System.out.println();
		 
		 }
			  System.out.println(i);
}
}
}